package com.xairlab.otus.message_system.entity;

public class WebMessage {

    private String name;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    private int age;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
